package ru.base.util;


import org.springframework.http.ResponseEntity;
import org.springframework.validation.BindingResult;
import ru.base.model.AbstractBaseEntity;
import ru.base.util.exception.IllegalRequestDataException;
import ru.base.util.exception.NotFoundException;

import java.util.stream.Collectors;

public class ValidationUtil {

    private ValidationUtil() {
    }

    public static <T> T checkNotFoundWithId(T object, int id) {
        return checkNotFound(object, "id=" + id);
    }

    public static void checkNotFoundWithId(boolean found, int id) {
        checkNotFound(found, "id=" + id);
    }

    public static <T> T checkNotFound(T object, String msg) {
        checkNotFound(object != null, msg);
        return object;
    }

    public static void checkNotFound(boolean found, String msg) {
        if (!found) {
            throw new NotFoundException("Not found entity with " + msg);
        }
    }

    public static void checkNew(AbstractBaseEntity entity) {
        if (!entity.isNew()) {
            throw new IllegalRequestDataException(entity + " must be new (id=null)");
        }
    }

    public static void assureIdConsistent(AbstractBaseEntity entity, int id) {
//      conservative when you reply, but accept liberally (http://stackoverflow.com/a/32728226/548473)
        if (entity.isNew()) {
            entity.setId(id);
        } else if (entity.getId() != id) {
            throw new IllegalRequestDataException(entity + " must be with id=" + id);
        }
    }


    public static ResponseEntity<String> getErrorResponse(BindingResult result) {
    return ResponseEntity.unprocessableEntity().body(
                    result.getFieldErrors().stream()
                                    .map(fe -> String.format("[%s] %s", fe.getField(), fe.getDefaultMessage()))
                            .collect(Collectors.joining("<br>"))
            );
    }


    
    //  http://stackoverflow.com/a/28565320/548473
    public static Throwable getRootCause(Throwable t) {
        Throwable result = t;
        Throwable cause;

        while (null != (cause = result.getCause()) && (result != cause)) {
            result = cause;
        }
        return result;
    }
}